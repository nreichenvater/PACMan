package jku.win.se.assignmentManager.backend.dto;

public class Dependency {

	private String type;
	private String id;
	
	public Dependency() {}
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
